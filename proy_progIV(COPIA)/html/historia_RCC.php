		 <h2 class="p"> HISTORIA DE LA RENOVACION CARISMATICA  </h2>
			
			<p class="c"><b>
				¿Qué es la Renovación Carismática?<p class="c">
				Es una acción del Espíritu Santo hoy renovando a toda la iglesia e intenta centrarse en el conocimiento interior de la vida personal y espiritual de la iglesia, que consiste en nacer de nuevo (Juan 3, 3) Es vivir la experiencia en otros criterios, actitudes y decisiones mediante la Efusión del Espíritu Santo.

<br>¿Cómo nace la Renovación Carismática?

<p class="c">Hechos 2, 1<br>

El 25 de diciembre de 1961 el Papa Juan XXIII (23) convoco al Concilio Vaticano II para transmitir la importancia de Pentecostés. – “Dígnese el divino Espíritu de la forma más consoladora la plegaria que asciende al mismo Espíritu desde todos los rincones de la tierra y que renueve en nuestros tiempos a todos los cristianos como un nuevo pentecostés” (oración del Papa Juan XXIII)

El 23 de octubre de 1963 el Cardenal  Leo Jozef  Suenens arzobispo de Lovaina, afirmaba que los carismas no son “un fenómeno accidental en la vida del hombre, sino que tiene su importancia vital en la fuerza del Espíritu Santo, dado a todos los fieles, pastores y laicos. En efecto sin el ministerio del Espíritu Santo los pastores, los carismas y el Magisterio de la Iglesia resultarían pobres, desordenados y estériles”.

En agosto de 1966 un grupo liderado por William Storey y Ralph Keifer, dos profesores laicos de la Universidad de Duquesne en Pittsburgh, Estados Unidos, decidieron orar juntos para pedir el bautismo en el Espíritu Santo. Por influencia de dos jóvenes laicos de los Cursillos de Cristiandad, Ralph Martin y Stephen B. Clark quienes habían leído sobre el bautismo de Pentecostés. En el otoño del mismo año unos jóvenes católicos de la misma universidad se reúnen con frecuencia para orar y crecer en enseñanzas de la fe lo cual les fortalecía, pero sentían aun un vacío en sus vidas.

En enero de 1967 el día de la Epifanía del Señor (6 de enero) los profesores se reúnen con el sacerdote episcopal Willian Lewis para hablar del Espíritu Santo, sus dones y carismas. El 20 de enero Ralph Keifer y Patricio Bourgois reunidos en la comunidad piden oración para recibir al Espíritu Santo y es entonces que Ralph recibe el don de lenguas y siente arder su corazón; lo cual movido por esa experiencia a la semana siguiente impone las manos a sus compañeros que se reunían para orar.

Del viernes 17 al domingo 19 de febrero de 1967 unas 30 personas hacen el primer Retiro de Inicio de vida en el Espíritu Santo en Duquesne, en donde Dios bendice este retiro con el don pleno del Espíritu Santo; recibiendo todos los presentes muchos carismas. 
Ya para inicios de 1968 se comienzan a formar gracias al testimonio de estos,  varios grupos de oración de Renovación Carismática en distintos lugares de Estados Unidos.

En el año de 1970 la Renovación Carismática empezó a ser difundida en América Latina por el padre Francis McNut, norteamericano graduado de la Universidad de Harvard; ya para 1971 la Renovación ya ha llegado a Centroamérica, Colombia y Chile.

El 10 de octubre de 1973 el Papa Pablo VI se reunión con un grupo de carismáticos para compartir experiencias de la Efusión del Espíritu Santo y fue ese mismo año que la Renovación Carismática fue aceptada como Movimiento Católico, 2 años más tarde en 1975 tiene su primer encuentro con 10 mil carismáticos manifestándoles su alegría y esperanza en el movimiento, proclamándolo así como “el alma de la Iglesia actual.”

¿Cómo nace la Renovación Carismática en Santiago de María? (probablemente en El Salvador)

En octubre de 1976 se forma un grupo de oración en Santiago de María, llamado Seguidores de Cristo; dirigido por el Fraile Cirilo Ruano (Fray Chilo) de la Orden Franciscana. Inicialmente se recibían 7 charlas de preparación y luego se formaban los grupos de oración en diferentes casas, llamados siempre “Seguidores de Cristo”

En 1978 llega a Santiago de María el Padre Fernando Cortez, quien era Fraile en ese entonces; quien al ver la forma, estructura y desarrollo de las reuniones de los grupos de oración dijo que eso era la Renovación Carismática; a partir de eso comenzaron a realizarse en la ciudad Retiros de Iniciación y se comienzan a formar ya Grupos de oración Carismáticos, (ya cada uno con un nombre elegido por sus miembros).
Esta experiencia ayudo a que se esparciera la Renovación por todo El Salvador ya que venían hermanos a tomar las enseñanzas traídas por el Fray Chilo y el padre Fernando que recibían en la ciudad de México de personas tales como: Pepe Prado, José Alonso y Dick Mischler;  y poder transmitirlas en sus parroquias; estos hermanos que visitaron Santiago de María para formarse fueron: Carlos Siris y Francisco Pérez Cano.

En ese mismo año se forma el primer Concejo de ancianos integrado por: Lito Salgado (papá), Mercedes de Salgado, Martha Beltrán, Carlos Segovia, Amadeo Castillo, entre otros.

El 4 de octubre de 1979 nace el primer ministerio de sanación en Santiago de María con los Esposos Salgado y Carlos Segovia. Este ministerio dejo de reunirse a causa de la guerra que azotaba al país pero en el año de 1985 se retoma la misión de iniciar labores espirituales como ministerio y este no ha parado de interceder por enfermos hasta la fecha.
Simultáneamente nace el Ministerio de Predicación formado por el hermano Salvador Gómez quien trabajo con la Renovación Carismática en la ciudad; cuando el Salvador se va de Santiago de María este ministerio dejo de funcionar y los pastores de grupos de oración asumieron la enseñanza de la palabra; hubo intentos por formar de nuevo el ministerio pero no fue tarea fácil; fue hasta el año 2003 que se organiza un grupo de hermanos con el carisma y la formación de predicadores y son los que hasta la fecha siguen trabajando en nuestro movimiento, incorporando cada cierto tiempo nuevos hermanos.

A partir de 1977 a 1981 se formó un grupo juvenil carismático, pero a raíz de la guerra los jóvenes dejaron de reunirse; ya para 1988 se vuelve a formar el grupo juvenil carismático llamado: Cristo Eternamente Joven. Logrando así en agosto de 1992 el primer Retiro de Iniciación para jóvenes en donde los que participaron fue un aproximado de 118 jóvenes que ardían por ser bautizados con el Espíritu Santo; haciendo un segundo retiro en 1994 con 109 jóvenes. En 1998 el grupo de jóvenes se desintegra por falta de coordinación y resurge a finales de 1999 manteniéndose hasta la fecha, cambiando su nombre a Grupo Juvenil Santiago Apóstol (en honor al Santo patrón de la Iglesia Catedral)

El ministerio de Misericordia que se encarga de visitar, alentar y fortalecer a enfermos no solo del movimiento sino de la ciudad nace también en el año 2003 y sigue trabajando por dar esa luz a los enfermos y necesitados.

Ministerio de alabanza siempre ha tenido nuestro movimiento, pero fue en el año 2004 que se forma el Ministerio de Alabanza Kyrios Yire (Significa “El Señor Provee”) nombre que le asignan sus miembros debido a que comenzaron a juntarse jóvenes sin mucho conocimiento musical para alabar a Dios y es así como los bendice Dios recibiendo ellos donaciones de instrumentos y equipo de sonido el cual sirvió para darle más motivación a nuestra comunidad.

El ministerio de Liturgia surge por la necesidad de que la Renovación Carismática se le asigna la misa dominical de 6:00 de la tarde y era necesario tener hermanos dispuestos a servir en el área litúrgica, es así como en el año 2007 se forma el ministerio y sigue en funciones hasta hoy en día, renovándose con sus miembros.

El ministerio de crecimiento surge a partir del año 2010; años anteriores los crecimientos eran dados por hermanos voluntarios que asumían este rol, pero que se guiaban solo por los libros de San Juan Bautista, hoy en día es un ministerio que se prepara para atender a cada grupo que sale de su retiro y darles el seguimiento oportuno y la enseñanza de los nuevos.

En el año 2014 se ve la necesidad de un Ministerio encargado de recibir y darles la bienvenida a todas las personas nuevas que llegan a nuestro movimiento, con el fin de hacerles sentir acogidos y en familia con nuestro movimiento, es asi como se conforma el Ministerio de Acogida a hermanos nuevos.

En Junio de 2015 se forma el Ministerio de Niños Angelitos de Dios, un ministerio que se encarga de catequizar en la fe y dar la enseñanza de nuestro movimiento a los hijos de hermanos que pertenecen a la Renovación Carismática.
	
